# "Load demo essay" button on the input screen.
module DemoEssay
  TEXT = <<~ESSAY.strip
    The Chocolate Tower and the Pink Dragon

    AI does not make everyone more creative. It makes everyone less bad.

    Small experiment.

    In a moment, close your eyes for thirty seconds. Picture a skyscraper made of chocolate. Build the details. The facade. The windows. The balconies. Dark chocolate or milk chocolate? Does it melt in the sun? Does the lobby smell like cacao?

    Start now.

    [Now imagine the picture: a pink dragon on a skateboard.]

    Now look at this pink dragon on a skateboard. Five seconds.

    Now close your eyes again. Imagine a pink dragon on a skateboard.

    What happens?

    For most people, the dragon from the picture takes over. You may edit a detail. A bigger board. A different background. A different shade of pink. But the starting point is no longer yours.

    This phenomenon has a name: design fixation. In 1991, Jansson and Smith gave engineering students example solutions before asking them to create new ones. The students copied features from those examples, including obvious flaws. The first impression became the gravitational centre. All later ideas orbited around it.

    Tversky and Kahneman had already shown in 1974 that judgments shift toward a reference point, even when that reference point is arbitrary. Anchoring.

    Using ChatGPT at the start of a creative task is like looking at the pink dragon. You get a first paragraph. An outline. Ideas for a project. A reference point to work from. Often solid. Sometimes impressive.

    You react to it. Refine it. Push it further. But once you have seen it, it becomes the thing you work from.

    Doshi and Hauser measured one version of this trade-off in 2024. They had participants write short stories, some with AI, some without. The AI-assisted stories were rated more creative and better written.

    But there is a second result. The stories were more similar to each other. Not dramatically. But measurably. Average individual quality went up. Collective diversity went down.

    If you struggle with idea generation, AI is a genuine lift. It pulls you past the blank page, past the first bad idea, toward something workable. The floor rises.

    But if you are someone who generates original ideas, who finds unexpected angles, who makes connections others miss, AI does something else. It pulls you toward the centre. Toward the average of what has already been said. Your edge may dull.

    AI does not make everyone more creative. It makes everyone less bad. Those are not the same thing.

    When everyone uses AI first, outputs converge. Doshi and Hauser measured it. The mechanism is simple: the models are trained on similar data, and they optimise for similar ideas of good.

    The pink dragon colonised your imagination in five seconds. Now imagine what happens to your outline, your argument, your design concept, before you have written a single line of your own.

    Before you open the tool, ask yourself one question.

    Do you want a slower start? The friction of not knowing yet, of writing badly first, of finding your own structure through the mess? That process is uncomfortable. It is also where original thinking lives.

    Or do you want fast convergence? A solid output, quickly, that others may well consider good. But it may not feel fully yours.

    Neither is wrong. But you need to decide. Task by task.

    Some tasks are fine to converge on. It was a perfectly decent dragon, after all. But maybe that is exactly the problem. Maybe you saw the decent dragon too early. Maybe it stopped you from finding the better one. The cooler one. The one below.
  ESSAY
end
